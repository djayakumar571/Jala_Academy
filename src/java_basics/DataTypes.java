package java_basics;

public class DataTypes {
	public static void main(String[] args){
		int num = 65;
		char alpha = 'J';
		float dec = 569.404f;
		double kum = 569.2541255;
		System.out.println("This is an integer: " + num);
		System.out.println("This is a float: " + dec);
		System.out.println("This is a char datatype: " + alpha);
		System.out.println("This is a double datatype: " + kum);
		
	}
}
